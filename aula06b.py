n = input('Digite algo:')
print('O Tipo primitivo é',type(n))
print('Só Tem Espaços?',n.isspace())
print('É Um Número?',n.isnumeric())
print('É Alfabético?',n.isalpha())
print('É Alfanumérico?',n.isalnum())
print('Esta Maiúsculas?',n.isupper())
print('Esta Minusculas?',n.islower())
print('Esta Capitalizado?',n.istitle())

 #Todos os is:
#alfabetico - isalpha
#numerico - isnumber
#só tem espaços - isspace
#alfabético e numérico - isalnum
#está em maiúsculas - isupper
#está em minúculas - islower
#está só com a primeira letra em maiúsulas - istitle

